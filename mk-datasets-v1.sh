#!/bin/bash

./mk-nanopubs.sh hurlbertlab dietdatabase

np mkindex \
  -o nanopubs/index.hurlbertlab_dietdatabase_nanopubs.trig.gz \
  -t "Nanopublications for the Avian Diet Database" \
  -d "This dataset was extracted from the Avian Diet Database being built by the Hurlbert Lab at the University of North Carolina. The nanopublications were generated by the elton tool of the Global Biotic Interactions initiative." \
  -a https://github.com/hurlbertlab/dietdatabase \
  -a http://labs.bio.unc.edu/Hurlbert/ \
  -a https://www.globalbioticinteractions.org \
  -a https://github.com/globalbioticinteractions/elton \
  -c 0000-0003-3138-4118 \
  -c 0000-0002-5678-9907 \
  nanopubs/hurlbertlab_dietdatabase_nanopubs.trig.gz

./mk-nanopubs.sh globalbioticinteractions aps-turfgrasses

np mkindex \
  -o nanopubs/index.globalbioticinteractions_aps-turfgrasses_nanopubs.trig.gz \
  -t "Nanopublications for Turfgrass Diseases" \
  -d "This dataset is about turfgrass diseases semi-automatically extracted from http://www.apsnet.org/publications/commonnames/Pages/Turfgrasses.aspx using the samara tool. The nanopublications were generated by the elton tool of the Global Biotic Interactions initiative." \
  -a https://github.com/globalbioticinteractions/aps-turfgrasses \
  -a https://github.com/jhpoelen/samara \
  -a http://www.apsnet.org/publications/commonnames/Pages/Turfgrasses.aspx \
  -a https://www.globalbioticinteractions.org \
  -a https://github.com/globalbioticinteractions/elton \
  -c 0000-0003-3138-4118 \
  nanopubs/globalbioticinteractions_aps-turfgrasses_nanopubs.trig.gz

./mk-nanopubs.sh globalbioticinteractions inaturalist

np mkindex \
  -o nanopubs/index.globalbioticinteractions_inaturalist_nanopubs.trig.gz \
  -t "Nanopublications for iNaturalist.org Species Interaction Observations" \
  -d "This dataset contains species interaction observations reported by iNaturalist.org. The nanopublications were generated by the elton tool of the Global Biotic Interactions initiative." \
  -a https://github.com/globalbioticinteractions/inaturalist \
  -a https://www.inaturalist.org \
  -a https://www.globalbioticinteractions.org \
  -a https://github.com/globalbioticinteractions/elton \
  -c 0000-0003-3138-4118 \
  nanopubs/globalbioticinteractions_inaturalist_nanopubs.trig.gz

./mk-nanopubs.sh globalbioticinteractions Catalogue-of-Afrotropical-Bees

np mkindex \
  -o nanopubs/index.globalbioticinteractions_Catalogue-of-Afrotropical-Bees_nanopubs.trig.gz \
  -t "Nanopublications for Catalogue of Afrotropical Bees" \
  -d "This dataset is about \"bees of sub-saharan Africa and the western Indian Ocean islands, excluding the honey bee (Apis mellifera Linnaeus)\". The nanopublications were generated by the elton tool of the Global Biotic Interactions initiative." \
  -a https://github.com/globalbioticinteractions/Catalogue-of-Afrotropical-Bees \
  -a http://doi.org/10.15468/u9ezbh \
  -a https://www.globalbioticinteractions.org \
  -a https://github.com/globalbioticinteractions/elton \
  -c 0000-0003-3138-4118 \
  nanopubs/globalbioticinteractions_Catalogue-of-Afrotropical-Bees_nanopubs.trig.gz

